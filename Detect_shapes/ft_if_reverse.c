#include "ft_lib.h"

int	ft_if_reverse(char *str)
{
	int i;
	int n1;
	int n2;

	i = 0;
	n2 = 0;
	while (str[i] != '\0')
	{
		n1 = 0;
		while (str[i] != '\n')
		{
			n1++;
			i++;
		}
		if (n2 == 0)
			n2 = n1 + 1;
		if (n1 < n2)
			n2 = n1;
		else
			return (0);
		i++;
	}
	return (1);
}
