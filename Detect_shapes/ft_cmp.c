int	ft_cmp(char *str1, char *str2)
{
	int i;

	i = 0;
	while ((str1[i] != '\0') || (str2[i] != '\0'))
	{
		if (str1[i] > str2[i])
		{
			return (1);
		}
		if (str1[i] < str2[i])
		{
			return (-1);
		}
		i++;
	}
	if (str1[i] == '\0' && str2[i] == '\0')
		return (0);
	else
		return (1);
}
