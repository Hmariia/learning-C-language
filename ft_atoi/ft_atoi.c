int		ft_atoi(char *str)
{
	int num;
	int i;

	num = 0;
	i = 0;
	while (str[i] != '\0' && str[i] - 48 >= 0 && str[i] - 48 < 10)
	{
		num = num + (str[i] - 48);
		i++;
		if (str[i] != '\0' && str[i] - 48 >= 0 && str[i] - 48 < 10)
		{
			num *= 10;
		}
	}
	return (num);
}
