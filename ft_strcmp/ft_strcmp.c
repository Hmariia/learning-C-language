int		ft_strcmp(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s1[i] != '\0')
	{
		if (s2[i] == s1[i])
		{
			i++;
		}
		else
			return (1);
	}
	if (s2[i] == '\0')
	{
		return (0);
	}
}
