void	ft_swap(int *a, int *b)
{
	int i;
	int temp;

	i = 0;
	while (a[i] != '\0' || b[i] != '\0')
	{
		temp = a[i];
		a[i] = b[i];
		b[i] = temp;
		i++;
	}
}
