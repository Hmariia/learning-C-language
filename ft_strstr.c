char	*ft_strstr(char *str, char *to_find)
{
	int j;
	int i;

	i = 0;
	j = 0;
	while (str[i] != '\0')
	{
		while (str[i] == to_find[j] && to_find != '\0')
		{
			j++;
			i++;
		}
		if (to_find[j] == '\0')
		{
			return (str + i - j);
		}
		j = 0;
		i++;
	}
	if (j == 0)
	{
		return (0);
	}
}
